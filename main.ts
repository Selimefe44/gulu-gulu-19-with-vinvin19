game.onUpdate(function () {
    for (let player22 of players) {
        // Yere değme kontrolü
        if (player22.y > groundLevel) {
            player22.y = groundLevel
            // Yere değince düşmeyi durdur
            player22.vy = 0
        }
        // Tavana çarpma kontrolü
        if (player22.y < ceilingLevel) {
            // Tavana çarpma durumu
            player22.y = ceilingLevel
            // Tavana çarpınca düşmeyi durdur
            player22.vy = 0
        }
    }
})
// Zarar mekanizması: Ebe ile çarpışma durumunda sağlık kaybı
game.onUpdate(function () {
    for (let i = 0; i <= players.length - 1; i++) {
        if (players[i] != ebe && players[i].overlapsWith(ebe)) {
            // Her çarpışmada 1 puanlık sağlık kaybı
            if (statusBars[i].value > 0) {
                statusBars[i].value -= 1;
            }
            // Eğer sağlık biterse, oyuncuyu elenmiş say ve sağlığı sıfırla
            if (statusBars[i].value <= 0) {
                game.splash("Oyuncu " + (i + 1) + " elendi!")
                players[i].destroy()
                statusBars[i].destroy()
                activePlayers.removeAt(i)
            }
        }
    }
})
